package za.ac.uj.acsse.miniproject.info;
/**
 * 
 * @author Broadkast
 *	Class will update discount on table
 *
 */
public class Charges {

	private double weekly;
	private double monthly;
	private double halfyearly;
	private double yearly;
	
	/**
	 * @return the weekly
	 */
	public double getWeekly() {
		return weekly;
	}
	/**
	 * @param weekly the weekly to set
	 */
	public void setWeekly(double weekly) {
		this.weekly = weekly;
	}
	/**
	 * @return the monthly
	 */
	public double getMonthly() {
		return monthly;
	}
	/**
	 * @param monthly the monthly to set
	 */
	public void setMonthly(double monthly) {
		this.monthly = monthly;
	}
	/**
	 * @return the halfyearly
	 */
	public double getHalfyearly() {
		return halfyearly;
	}
	/**
	 * @param halfyearly the halfyearly to set
	 */
	public void setHalfyearly(double halfyearly) {
		this.halfyearly = halfyearly;
	}
	/**
	 * @return the yearly
	 */
	public double getYearly() {
		return yearly;
	}
	/**
	 * @param yearly the yearly to set
	 */
	public void setYearly(double yearly) {
		this.yearly = yearly;
	}
	
}
